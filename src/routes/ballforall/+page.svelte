<h1>We ask for one commitment – Ball for All</h1>

<div class="container">
  <p>If anyone (player or coach) wants to be involved in the youth club, they must commit to the Ball for All principles and hold everyone to account for them. They create great and enjoyable basketball.

    To join, a player must discuss these principles with the coaches and team-mates, sign them, and play by them.
    No player will be asked to leave the club unless they do not follow these principles:</p>
</div>

<div class="box">
  <div class="innerBox">
    1) Joy – it's the essence of success<br>
    2) Positivity – positive mental attitude is key<br>
    3) Respect – we treat others like we would like to be treated<br>
    4) Equal Focus and Equal Minutes – we are all part of this team<br>
    5) Dedication and effort – It's how we get better<br>
    6) Growth – It's where we are now, and where we will be in the future<br>
    7) Pass – because ball hogging ruins the game<br>
    8) Defence – It wins championships<br>
    9) Shoot – when it's good and within range. The only miss is not taking a good shot<br>
    10) Play – together as a team.<br>
  </div>
</div>

<p class="B4A">Because it's ball for all, not just some.</p>

